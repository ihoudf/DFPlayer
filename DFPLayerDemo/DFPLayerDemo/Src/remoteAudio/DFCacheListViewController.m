@interface DFCacheListViewController ()
@property (nonatomic, strong) NSMutableArray<DFPlayerModel *> *cacheArray;

@end

@implementation DFCacheListViewController

- (void)viewDidLoad {
    [super viewDidLoad];
    self.view.backgroundColor = [UIColor whiteColor];
    self.navigationItem.title = @"缓存列表";
    
    // Uncomment the following line to preserve selection between presentations.
    // self.clearsSelectionOnViewWillAppear = NO;
    
